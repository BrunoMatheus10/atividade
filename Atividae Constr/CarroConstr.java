public class CarroConstr {
    String modelo;
    String marca;
    String ano;
    String motor;
    String cambio;
    String freio;
    String suspensao;
    String rodas;
    String peso;
    String desempenho;

    public CarroConstr(String modelo, String marca, String ano, String motor,
                 String cambio, String freio, String suspensao,
                 String rodas, String peso, String desempenho) {
        this.modelo = modelo;
        this.marca = marca;
        this.ano = ano;
        this.motor = motor;
        this.cambio = cambio;
        this.freio = freio;
        this.suspensao = suspensao;
        this.rodas = rodas;
        this.peso = peso;
        this.desempenho = desempenho;
    }
}